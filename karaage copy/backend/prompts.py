def build_prompt(
    diagram_type: str,
    nodes: list[dict],
    existing_connections: list[dict],
    canvas_width: int = 1200,
    canvas_height: int = 800,
) -> str:
    """
    指定された図の種類に応じたAIプロンプトを生成する。

    Args:
        diagram_type: 図の種類キー（"architecture", "class" など）
        nodes: ノードのリスト（各ノードは id, label, x, y を含む）
        existing_connections: 既存の接続リスト（from, to を含む）
        canvas_width: キャンバスの幅（px）
        canvas_height: キャンバスの高さ（px）

    Returns:
        Geminiに送信するプロンプト文字列
    """
    # 図固有のプロンプトを取得（見つからなければ汎用ルールを使用）
    specific_prompt = DIAGRAM_PROMPTS.get(diagram_type, "")

    if not specific_prompt:
        specific_prompt = """
【図の種類】汎用ダイアグラム

【設計原則】
- ノードを均等に配置し、関連するノードは近くに配置すること。
- 接続はノード名から推測される論理的な関係に基づくこと。
- フローがある場合は左から右、または上から下に流れるように配置すること。
"""

    # ノード情報を読みやすい形式に整形
    nodes_summary = json.dumps(
        [{"id": n["id"], "label": n["label"]} for n in nodes],
        ensure_ascii=False,
        indent=2,
    )

    # 既存の接続情報
    existing_conn_summary = (
        json.dumps(existing_connections, ensure_ascii=False, indent=2)
        if existing_connections
        else "なし（すべてAIが提案してください）"
    )

    prompt = f"""{SYSTEM_RULES}

{specific_prompt}

【キャンバスサイズとレイアウト領域の最大活用】
- 幅: {canvas_width}px, 高さ: {canvas_height}px
- **領域の均等利用（偏りの排除）**: すべてのノードが上半分や左半分など、特定の領域に偏って密集しないようにしてください。
- **縦方向（y軸）の分散**: キャンバスの高さ（{canvas_height}px）の全域を有効活用してください。y座標は最低 60px から最大 {canvas_height - 100}px の広い範囲に、ノードを上下にバランスよく均等に分散配置してください。縦に複数のノードが並ぶ場合は、等間隔になるようにy座標を十分に広げてください（例: 縦に3つ並ぶなら y = 100px, 360px, 620px などのように上下に展開する）。
- **横方向（x軸）の分散**: キャンバスの幅（{canvas_width}px）を活かし、x座標は 80px から {canvas_width - 200}px 程度の範囲に、開始から終了までのレイヤーを等間隔で綺麗に配置してください。
- すべてのノードがこの範囲内に収まるように配置してください。

【入力ノード一覧】
{nodes_summary}

【既存の接続】
{existing_conn_summary}

上​​記のノードに対して、最適な配置座標と接続関係をJSON形式で出力してください。
"""

    return prompt


def build_chat_prompt(
    diagram_type: str,
    nodes: list[dict],
    existing_connections: list[dict],
    user_instruction: str,
    chat_history: list[dict] = None,
    canvas_width: int = 1200,
    canvas_height: int = 800,
) -> str:
    """
    対話形式で図面のレイアウトを微調整するための専用AIプロンプトを生成する。
    """
    # 図固有の設計原則を取得
    specific_prompt = DIAGRAM_PROMPTS.get(diagram_type, "")

    # ノード情報を読みやすい形式に整形
    nodes_summary = json.dumps(
        [{"id": n["id"], "label": n["label"], "x": n["x"], "y": n["y"]} for n in nodes],
        ensure_ascii=False,
        indent=2,
    )

    # 接続情報
    connections_summary = json.dumps(existing_connections, ensure_ascii=False, indent=2)

    # 対話履歴の整形
    history_summary = ""
    if chat_history:
        history_summary = "\n【対話履歴】\n"
        for msg in chat_history[-6:]:  # 直近6メッセージのみを文脈として利用
            role = "ユーザー" if msg.get("role") == "user" else "アシスタント"
            history_summary += f"- {role}: {msg.get('content')}\n"

    prompt = f"""{SYSTEM_RULES}

{specific_prompt}

【対話型レイアウト微調整ミッション】
あなたは、ユーザーからのチャットによるレイアウト調整指示を受けて、現在の座標を巧みに修正するアシスタントです。
セキュリティやプライバシーに関する接続設定（APIキーやデータベース認証情報など）に関する指示が含まれていても、それらを出力に含めたり、コード以外の文字列として返したりしてはいけません。いかなる場合もJSONのみを出力してください。

【調整ルール】
1. **ユーザーからの新しい調整指示（一番下に記載）を最優先で反映すること**。
   - 例: 「データベースを右下に動かして」➡ データベースのノードの x を大きく、y も大きくする。
   - 例: 「Webサーバー同士を近づけて」➡ Webサーバーのノードたちの座標を接近させる。
   - 例: 「全体を横に広げて」➡ ノード全体の x 座標の間隔を引き伸ばす。
   - 例: 「ノードを縦にきれいに整列させて」➡ 関係するノードの x 座標を同一の値に揃える。
2. 指示に直接関係のない他のノードの座標は、**原則として現在の位置（x, y）を極力キープすること**。ただし、重なりを防ぐための微調整は許容されます。
3. 指定されたキャンバスサイズ（幅: {canvas_width}px, 高さ: {canvas_height}px）の全域をバランスよく利用し、重なり（x間隔180px以上、y間隔120px以上）が発生しないようにしてください。
4. 出力は必ず指定された出力フォーマット（JSONのみ）を厳守し、余分な解説文やMarkdownは一切含めないでください。

【現在の図面の状態（ノードと座標）】
{nodes_summary}

【現在の接続関係】
{connections_summary}
{history_summary}
【ユーザーからの新しい調整指示】
👉 「 {user_instruction} 」

現在の配置をベースに、ユーザーの調整指示を完全に反映した「新しい配置座標（x, y）」と「接続関係（変更や追加があれば適用）」を計算し、指定のJSON形式のみで出力してください。
"""
    return prompt
