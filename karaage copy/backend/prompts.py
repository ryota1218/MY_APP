def build_prompt(
    diagram_type: str,
    nodes: list[dict],
    existing_connections: list[dict],
    canvas_width: int = 1200,
    canvas_height: int = 800,
) -> str:
    """
    指定された図の種類に応じたAIプロンプトを生成する。

    Args:
        diagram_type: 図の種類キー（"architecture", "class" など）
        nodes: ノードのリスト（各ノードは id, label, x, y を含む）
        existing_connections: 既存の接続リスト（from, to を含む）
        canvas_width: キャンバスの幅（px）
        canvas_height: キャンバスの高さ（px）

    Returns:
        Geminiに送信するプロンプト文字列
    """
    # 図固有のプロンプトを取得（見つからなければ汎用ルールを使用）
    specific_prompt = DIAGRAM_PROMPTS.get(diagram_type, "")

    if not specific_prompt:
        specific_prompt = """
【図の種類】汎用ダイアグラム

【設計原則】
- ノードを均等に配置し、関連するノードは近くに配置すること。
- 接続はノード名から推測される論理的な関係に基づくこと。
- フローがある場合は左から右、または上から下に流れるように配置すること。
"""

    # ノード情報を読みやすい形式に整形
    nodes_summary = json.dumps(
        [{"id": n["id"], "label": n["label"]} for n in nodes],
        ensure_ascii=False,
        indent=2,
    )

    # 既存の接続情報
    existing_conn_summary = (
        json.dumps(existing_connections, ensure_ascii=False, indent=2)
        if existing_connections
        else "なし（すべてAIが提案してください）"
    )

    prompt = f"""{SYSTEM_RULES}

{specific_prompt}

【キャンバスサイズとレイアウト領域の最大活用】
- 幅: {canvas_width}px, 高さ: {canvas_height}px
- **領域の均等利用（偏りの排除）**: すべてのノードが上半分や左半分など、特定の領域に偏って密集しないようにしてください。
- **縦方向（y軸）の分散**: キャンバスの高さ（{canvas_height}px）の全域を有効活用してください。y座標は最低 60px から最大 {canvas_height - 100}px の広い範囲に、ノードを上下にバランスよく均等に分散配置してください。縦に複数のノードが並ぶ場合は、等間隔になるようにy座標を十分に広げてください（例: 縦に3つ並ぶなら y = 100px, 360px, 620px などのように上下に展開する）。
- **横方向（x軸）の分散**: キャンバスの幅（{canvas_width}px）を活かし、x座標は 80px から {canvas_width - 200}px 程度の範囲に、開始から終了までのレイヤーを等間隔で綺麗に配置してください。
- すべてのノードがこの範囲内に収まるように配置してください。

【入力ノード一覧】
{nodes_summary}

【既存の接続】
{existing_conn_summary}

上​​記のノードに対して、最適な配置座標と接続関係をJSON形式で出力してください。
"""

    return prompt
